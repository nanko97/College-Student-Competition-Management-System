-- =====================================================
-- 4. 晋级规则说明
-- =====================================================
-- 字段说明：
-- fu_jingsai_id: 父竞赛ID（低级别竞赛，如校级）
-- zi_jingsai_id: 子竞赛ID（高级别竞赛，如省级）
-- fu_jibie: 父竞赛级别（校级/省级）
-- zi_jibie: 子竞赛级别（省级/国家级）
-- jinji_type: 晋级类型（自动晋级/申请晋级）
-- jinji_rule: 晋级规则说明（详细描述晋级条件）
-- zuidi_fenshu: 最低分数要求（85分或90分）
-- zuidi_mingci: 最高名次要求（前3名或前5名）
-- is_active: 是否启用（是/否）
--
-- 晋级条件逻辑：
-- 1. 名次要求：必须达到指定名次以内（zuidi_mingci）
-- 2. 分数要求：必须达到最低分数线（zuidi_fenshu）
-- 3. 两个条件必须同时满足才能晋级
-- =====================================================

-- =====================================================
-- 5. 验证查询
-- =====================================================

-- 查看所有晋级关系
SELECT 
    id,
    fu_jingsai_id AS '父竞赛ID(低级)',
    zi_jingsai_id AS '子竞赛ID(高级)',
    fu_jibie AS '父级别',
    zi_jibie AS '子级别',
    jinji_type AS '晋级类型',
    jinji_rule AS '晋级规则',
    zuidi_fenshu AS '最低分数',
    zuidi_mingci AS '最高名次',
    is_active AS '是否启用'
FROM jingsai_jinji_guanxi
ORDER BY id;

-- 统计各类型晋级路径数量
SELECT 
    fu_jibie,
    zi_jibie,
    COUNT(*) AS '路径数量',
    AVG(zuidi_fenshu) AS '平均分数要求',
    AVG(zuidi_mingci) AS '平均名次要求'
FROM jingsai_jinji_guanxi
WHERE is_active = '是'
GROUP BY fu_jibie, zi_jibie;

-- =====================================================
-- 6. 晋级业务逻辑建议
-- =====================================================
-- 6.1 自动晋级流程：
--    1. 系统定期扫描已完成的比赛成绩
--    2. 筛选满足晋级条件的学生（名次+分数）
--    3. 自动创建晋级记录并标记为"待审核"
--    4. 教师/管理员审核后正式晋级
--
-- 6.2 申请晋级流程：
--    1. 学生主动提交晋级申请
--    2. 系统验证是否满足晋级条件
--    3. 教师/管理员审核申请
--    4. 审核通过后创建晋级记录
--
-- 6.3 晋级状态流转：
--    未晋级 → 待审核 → 已通过/已驳回
-- =====================================================

-- =====================================================
-- 7. 数据完整性检查
-- =====================================================

-- 检查是否有孤儿记录（关联的竞赛不存在）
SELECT '孤儿记录检查' AS check_type, COUNT(*) AS count
FROM jingsai_jinji_guanxi g
LEFT JOIN jingsaixinxi j1 ON g.fu_jingsai_id = j1.id
LEFT JOIN jingsaixinxi j2 ON g.zi_jingsai_id = j2.id
WHERE j1.id IS NULL OR j2.id IS NULL;

-- 检查晋级路径是否完整（校级→省级→国家级）
SELECT 
    fu_jibie,
    zi_jibie,
    GROUP_CONCAT(CONCAT(fu_jingsai_id, '→', zi_jingsai_id)) AS '晋级路径'
FROM jingsai_jinji_guanxi
WHERE is_active = '是'
GROUP BY fu_jibie, zi_jibie;

